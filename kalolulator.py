print("MASUKAN ANGGKA : ")
angka_1 = int(input())
angka_2 = int(input())
angka_3 = int(input())

print("PILIH OPERAOR")
jumlah = input()
if jumlah == "+" :
    print(angka_1 + angka_2 + angka_3)
elif jumlah == "-" :
    print(angka_1 - angka_2 - angka_3)
elif jumlah == "*" :
    print(angka_1 * angka_2 * angka_3)
elif jumlah == "/" :
    print(angka_1 / angka_2 / angka_3)
    




    
